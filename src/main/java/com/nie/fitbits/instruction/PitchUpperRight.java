package com.nie.fitbits.instruction;

/**
 * Holding the parsed upper and right instruction
 * 
 * @author lnie
 *
 */
public class PitchUpperRight {
	
	private int right;
	private int upper;
	
	public int getRight() {
		return right;
	}
	public void setRight(int right) {
		this.right = right;
	}
	public int getUpper() {
		return upper;
	}
	public void setUpper(int upper) {
		this.upper = upper;
	}

}
